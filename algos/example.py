__all__ = ['summation']


def summation(a, b):
    """
    given two values, 'a' and 'b' the sum of these values are returned
    parameters
    ----------
    a : float or integer
        any value
    b : float or integer
        any value
    examples
    --------
    >>> a = 2.0
    >>> b = 3.0
    >>> c = summation(a,b)
    >>> c
    5.0
    """
    return a+b
